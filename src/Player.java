/**
 * Keeps track of which player
 */
public enum Player {

    /**
     * Human player
     */
    HUMAN,

    /**
     * AI player
     */
    AI,

    /**
     * Player one
     */
    PLAYER_ONE,

    /**
     * Player two
     */
    PLAYER_TWO,

    /**
     * Neutral - if that location has not been played but neither of the players
     */
    NEUTRAL,
}
